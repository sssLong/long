// 获取子列表
function getChilds(elem, tag) {
    try {
        var childs = new Array();
        var nodes = elem.childNodes;
        for (var i = 0; i < nodes.length; i++) {
            if (typeof nodes[i].tagName != 'undefined' && nodes[i].tagName.toLowerCase() == tag.toLowerCase()) {
                childs[childs.length] = nodes[i];
            }
        }
        return childs;
    } catch (e) {
        return elem.getElementsByTagName(tag);
    }
}

var ul = document.getElementById('ul_nav');
var lis = getChilds(ul, 'li');

for (var i = 0; i < lis.length; i++) {
    var elem = lis[i];
    elem.onclick = function() {
        //
        var id = this.getAttribute('val');
        var conElem = document.getElementById(id);
        if (conElem != null) {
            tagConElem(this, conElem);
        } else {
            alert('对不起,该职位暂无相关数据.');
        }
    }
}

var div = document.getElementById('div_con');
var divs = getChilds(div, 'div');

// 点击事件
function tagConElem(elem, conElem) {

    for (var i = 0; i < lis.length; i++) {
        lis[i].getElementsByTagName('a')[0].className = '';
    }

    elem.getElementsByTagName('a')[0].className = 'current';

    for (var i = 0; i < divs.length; i++) {
        divs[i].style.display = 'none';
    }

    conElem.style.display = '';

}

